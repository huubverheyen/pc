package be.pxl.h5.opdr7;

public class H5opdr7 {

	public static void main(String[] args) {
		boolean waarde1 = true;
		boolean waarde2 = false;
		
		System.out.println("waarde1            : " + waarde1);
		System.out.println("waarde2            : " + waarde2);
		System.out.println("waarde1 && waarde2 : " + (waarde1 && waarde2));
		System.out.println("waarde1 || waarde2 : " + (waarde1 || waarde2));
		System.out.println("!waarde2           : " + waarde2);
		System.out.println("!waarde2           : " + waarde2);
	}

}
